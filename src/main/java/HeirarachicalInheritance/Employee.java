package HeirarachicalInheritance;

public class Employee {
	int empId;
	String name;
	

	public void setId(int id) {
		this.empId=id;
	}
	public int getId(){
		return empId;
	}
	public void setName(String name) {
		this.name=name;
	}
	public String getName() {
		return name;
	}
}
